BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "SURVEY_LANGUAGE" (
	"LANGUAGE_ID"	TEXT,
	"LANGUAGE"	TEXT,
	PRIMARY KEY("LANGUAGE_ID")
);
CREATE TABLE IF NOT EXISTS "PERMISSION" (
	"PERMISSION_ID"	TEXT,
	"PERMISSION_NAME"	TEXT,
	PRIMARY KEY("PERMISSION_ID")
);
CREATE TABLE IF NOT EXISTS "QUESTION_TYPE" (
	"QUE_TYPE_ID"	TEXT NOT NULL,
	"QUESTION_NAME"	TEXT,
	PRIMARY KEY("QUE_TYPE_ID")
);
CREATE TABLE IF NOT EXISTS "OFFERED_ANSWER" (
	"OFFERED_ANSWER_ID"	TEXT,
	"ANSWER_TEXT"	TEXT,
	PRIMARY KEY("OFFERED_ANSWER_ID")
);
CREATE TABLE IF NOT EXISTS "RESPONDENT" (
	"RESPONDENT_ID"	TEXT,
	"FIRST_NAME"	TEXT,
	"LAST_NAME"	TEXT,
	"EMAIL_ID"	TEXT,
	"CREATED_IS"	TEXT,
	"IP_ADDRESS"	TEXT,
	PRIMARY KEY("RESPONDENT_ID")
);
CREATE TABLE IF NOT EXISTS "RESPONSE" (
	"RESPONDENT_ID"	TEXT,
	"QUESTION_ID"	TEXT,
	"SURVEY_ID"	TEXT,
	"OFFERED_ANSWER_ID"	TEXT
);
CREATE TABLE IF NOT EXISTS "USER_ROLE" (
	"USER_ROLE_ID"	TEXT,
	"NAME"	TEXT,
	"DESCRIPTION"	TEXT,
	PRIMARY KEY("USER_ROLE_ID")
);
CREATE TABLE IF NOT EXISTS "ROLE_PERMISSION" (
	"USER_ROLE_ID"	TEXT,
	"PERMISSION_ID"	TEXT,
	FOREIGN KEY("PERMISSION_ID") REFERENCES "PERMISSION"("PERMISSION_ID"),
	FOREIGN KEY("USER_ROLE_ID") REFERENCES "USER_ROLE"("USER_ROLE_ID")
);
CREATE TABLE IF NOT EXISTS "USER" (
	"USER_ID"	TEXT NOT NULL,
	"FIRST_NAME"	TEXT,
	"LAST_NAME"	TEXT,
	"EMAIL_ID"	TEXT,
	"USER_ROLE_ID"	TEXT,
	"GROUP_ID"	TEXT,
	FOREIGN KEY("GROUP_ID") REFERENCES "GROUPS"("GROUP_ID"),
	FOREIGN KEY("USER_ROLE_ID") REFERENCES "USER_ROLE"("USER_ROLE_ID"),
	PRIMARY KEY("USER_ID")
);
CREATE TABLE IF NOT EXISTS "SURVEY" (
	"SURVEY_ID"	TEXT,
	"GROUP_ID"	TEXT,
	"NAME"	TEXT,
	"START_DATE"	TEXT,
	"END_DATE"	TEXT,
	"LAST_UPDATED"	TEXT,
	"LANGUAGE_ID"	TEXT,
	"CATEGORY_ID"	TEXT,
	FOREIGN KEY("LANGUAGE_ID") REFERENCES "SURVEY_LANGUAGE"("LANGUAGE_ID"),
	FOREIGN KEY("CATEGORY_ID") REFERENCES "SURVEY_CATEGORY"("CATEGORY_ID"),
	FOREIGN KEY("GROUP_ID") REFERENCES "GROUPS"("GROUP_ID"),
	PRIMARY KEY("SURVEY_ID")
);
CREATE TABLE IF NOT EXISTS "QUESTION" (
	"QUESTION_ID"	TEXT,
	"QUESTION_TEXT"	TEXT,
	"QUE_TYPE_ID"	TEXT,
	FOREIGN KEY("QUE_TYPE_ID") REFERENCES "QUESTION_TYPE"("QUE_TYPE_ID"),
	PRIMARY KEY("QUESTION_ID")
);
CREATE TABLE IF NOT EXISTS "SURVEY_QUESTION" (
	"QUESTION_ID"	TEXT,
	"SURVEY_ID"	TEXT,
	FOREIGN KEY("SURVEY_ID") REFERENCES "SURVEY"("SURVEY_ID"),
	FOREIGN KEY("QUESTION_ID") REFERENCES "QUESTION"("QUESTION_ID")
);
CREATE TABLE IF NOT EXISTS "SURVEY_QUESTION_ANSWER" (
	"QUESTION_ID"	TEXT,
	"SURVEY_ID"	TEXT,
	"OFFERED_ANSWER_ID"	TEXT,
	FOREIGN KEY("QUESTION_ID") REFERENCES "QUESTION"("QUESTION_ID"),
	FOREIGN KEY("SURVEY_ID") REFERENCES "SURVEY_QUESTION"("SURVEY_ID"),
	FOREIGN KEY("OFFERED_ANSWER_ID") REFERENCES "OFFERED_ANSWER"("OFFERED_ANSWER_ID")
);
CREATE TABLE IF NOT EXISTS "GROUPS" (
	"GROUP_ID"	TEXT,
	"GROUP_NAME"	TEXT,
	"GROUP_DESCRIPTION"	TEXT,
	PRIMARY KEY("GROUP_ID")
);
CREATE TABLE IF NOT EXISTS "SURVEY_CATEGORY" (
	"CATEGORY_ID"	TEXT,
	"CATEGORY_NAME"	TEXT,
	PRIMARY KEY("CATEGORY_ID")
);
